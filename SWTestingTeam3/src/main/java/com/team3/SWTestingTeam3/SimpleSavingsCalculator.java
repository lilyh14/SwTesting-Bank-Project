package com.team3.SWTestingTeam3;

public class SimpleSavingsCalculator {
  float intialDeposit;
  float monthlyContribution;
  float years;
  float simpleInterest;

  public SimpleSavingsCalculator(
      float intialDeposit, float monthlyContribution, float years, float simpleInterest) {
    this.intialDeposit = intialDeposit;
    this.monthlyContribution = monthlyContribution;
    this.years = years;
    this.simpleInterest = simpleInterest;
  }
}
